#Operadores aritmeticos
print(10 + 10)
print(10 - 5)
print(10 * 2)
print(10 / 2)

#Operador modulo o residuo
print(10 % 2)
print(10 / 3)
print(10 % 3)

#Operador // que es la division pero solo captura el numero entero
print(10 // 3)

#Operador exponencial
print(2 ** 3)

#Operacion compleja
#Para resolver las operaciones usamos "pemdas" y de izquierda a derecha: Parentesis, Exponentes, Multiplicacion, Division, Adicion, Sustraccion
print(2 ** 3 + 3 - 7 / 1 // 4)
#Explicacion:
print(2 ** 3)
print((7 / 1) // 4)
print(8 + 3 - 1)